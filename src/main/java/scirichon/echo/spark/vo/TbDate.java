package scirichon.echo.spark.vo;

public class TbDate {
	private String dateId;
	private int years;
	private int	theYear;
	private int month;
	private int day;
	private int weekday;
	private int week;
	private int quarter;
	private int period;
	private int halfMonth;
	public String getDateId() {
		return dateId;
	}
	public void setDateid(String dateId) {
		this.dateId = dateId;
	}
	public int getYears() {
		return years;
	}
	public void setYears(int years) {
		this.years = years;
	}
	public int getTheYear() {
		return theYear;
	}
	public void setTheYear(int theYear) {
		this.theYear = theYear;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getWeekday() {
		return weekday;
	}
	public void setWeekday(int weekday) {
		this.weekday = weekday;
	}
	public int getWeek() {
		return week;
	}
	public void setWeek(int week) {
		this.week = week;
	}
	public int getQuarter() {
		return quarter;
	}
	public void setQuarter(int quarter) {
		this.quarter = quarter;
	}
	public int getPeriod() {
		return period;
	}
	public void setPeriod(int period) {
		this.period = period;
	}
	public int getHalfMonth() {
		return halfMonth;
	}
	public void setHalfMonth(int halfMonth) {
		this.halfMonth = halfMonth;
	}
}
